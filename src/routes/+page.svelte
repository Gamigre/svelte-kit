      <h2 id="showcase_title">Diplôme de DNSEP, Remanenc.es (2022)</h2>
     <div id="showcase_desc_wrapper">
      <p id="showcase_desc"> Ces carreaux de céramique, tous porteurs d'un QR Code différent, sont destinés à être posés un peu partout, dans l’espace public comme privé. Chaque carreau permet d'accéder à un espace numérique qui lui est dédié, dans lequel se trouve une représentation en 3D de ce même lieu.<br>Cette représentation photogrammétrique permet de préserver le lieu des affres du temps, et surtout, vient recueillir les souvenirs des visiteurs. En effet, lorsque l'on accède à cet espace via le QR Code, on peut déposer une trace de son passage et raconter ce qui nous lie à ce lieu; des bribes de conversations, une recette, une sensation, une histoire très précise ou un simple mot; un texte, une image, un enregistrement audio sur le vif, ou les trois à la fois, la diversité des moyens d'expression tente de ne pas limiter celle du souvenir, qui est, par définition, une réinterprétation sans cesse renouvelée d'un évènement passé. <br> Ces traces seront ensuite consultables au milieu de la représentation du lieu par les prochains visiteurs et contribuent ainsi à former et sauvegarder cette mémoire collective. Le dispositif est conçu pour être facilement déployé et reproductible, tant dans la conception procédurale des carreaux qui peuvent être refaits avec le matériel d'un fablab, que celle de la plateforme numérique, dont le code est open-source.<br>
        Il est déjà déployé dans plusieurs lieux, des catacombes de Paris à une université en Suède, en passant par l'Esad et par chez ma grand-mère. <br><br> Sous la direction de Mathieu Ehrsam, Laurent Mészáros et Olaf Avenati. Merci aussi à Clara Daumars, Amélie Blachère et Amélie Chantraine pour les photos additionnelles.
